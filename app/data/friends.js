// NODE.JS SERVER USING EXPRESS FRAMEWOR
const express = require("express"); // LIBRARY 
const app = express(); // CALL EXPRESS WHICH IS SUBSTANCIATING EXPRESS LIBRARY

// SET THE PORT TO OUR APPLICATION 
    // PROCESS.ENV.PORT LETS THE ORT BE SET BY HEROKU 
const PORT = process.env.PORT || 3000;

// SETS UP THE EXPRESS APP TO HANDLE DATA PARSING. EXPRESS DOES NOT NATIVELY PARSE DATA 

// LISTEN FUNCTION; START OUR SERVER SO TAHT IT CAN BEGIN LISTENING TO CLIENT REQUESTS. 
app.listen(PORT, function(){
    // LOG (SERVER-SIDE) WHEN OUR SERVER HAS STARTED
    console.log("Server is listenning on:" + PORT);
})